                                <p>A felhasználók, a galéria valamint az egyes modulokban feltöltött képeket a rendszer a megfelelő méretben tölti fel a szerverre (általában egy kisebb és egy nagyobb méretben), és az UPLOADS mappa megfelelő almappáiba kerülnek.   
